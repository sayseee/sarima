<h2 class="title3">About us</h2>
SARIMA is a membership organisation of Research and Innovation Managers that operates at an institutional, national
and international level, as well as across the value chain, from research through to successful innovation (commercialization).
The purpose of the association is to promote research and innovation management for the benefit of southern Africa. SARIMA is 
engaged on an annual basis with a large number of research and innovation management professionalization activities, including 
capacity development courses and other interventions. SARIMA manages a cohort of Southern African Development community (SADC)
focal points to promote research and innovation management in collaboration with the South African Department of Science and Technology. 
It furthermore manages and co-ordinates a growing portfolio of multilateral programmes and projects in support of the SARIMA mandate and
for the benefit of its members and stakeholders.
